<p>Conditions générales (de vente ou d'utilisation) d'un site internet
Ce modèle vous permet d'établir à la fois, les conditions générales d'utilisation d'un site internet et les conditions générales de vente du site, si votre activité est la vente en ligne. <br> <br>


Ce document vous permet d'établir des conditions générales d'utilisation (CGU) afin d'informer les internautes naviguant sur votre site web (site, blog, page, etc) des modalités d'utilisation de votre site. <br> <br>

Les CGU fonctionnent comme un contrat par lequel un éditeur de site web (celui qui administre et gère le site internet) informe l'utilisateur ou internaute qui utilise ce site, des droits, obligations et responsabilités réciproques.

Les CGU ne doivent pas être confondues avec des conditions générales de vente qui relèvent de la relation commerciale, à savoir les droits et obligations de celui qui vend et de celui qui achète sur un site internet. <br> <br>


Il vous permettra également d'établir des conditions générales de vente (CGV) qui vont définir les modalités de vente de biens et produits par le biais du web en rappelant le cadre légal comme les droits et obligations contractuels s'appliquant au vendeur et à l'acheteur. Ces conditions générales de vente (CGV) permettent ainsi de régir les rapports entre le vendeur et l'acheteur sur internet et de délimiter leur responsabilité. <br> <br>

Dans ce modèle, vous trouverez les informations légales et obligatoires dès qu'un site internet a une activité commerciale mais aussi les modalités contractuelles d'exécution de la vente, notamment : <br> <br>

les modes de livraison et de paiement
les modes d'échange ou de remboursement
le contact avec le service client
Ces CGV doivent être validées manuellement par vos clients afin de vous assurer de leur consentement aux conditions de vente et, le cas échéant, pouvoir démontrer ce consentement. <br> <br>


Comment utiliser ce document ? <br>

Conditions générales d'utilisation
Une partie de ce document contient des dispositions légales obligatoires qu'il faudra nécessairement remplir. <br><br>

Pour que les CGU acquièrent une valeur contractuelle, l'éditeur du site doit prévoir une modalité électronique d'acceptation (par exemple une fenêtre avec les CGU à accepter par une case à cocher et qui s'ouvre avant toute utilisation du site). <br> <br>

Des espaces de rédaction libres sont prévues dans le document concernant l'objet de votre site internet qu'il est préférable de préciser et d'adapter à votre cas en vous rappelant que cet objet ne doit pas être illicite et discriminatoire. <br> <br>

Si votre site internet collecte les données personnelles des utilisateurs, selon les cas, une déclaration de traitement automatisé des données doit être déclaré à la Commission nationale de l'informatique et des libertés (CNIL). Le numéro de votre déclaration devra être précisé dans les CGU. <br>

Enfin, l'éditeur du site doit prévoir une adresse mail valide et régulièrement consultée afin de réagir promptement à tout signalement de contenu illicite sur son site. <br>


Conditions générales de vente <br>
Les CGV doivent, pour être opposables au client, être préalablement signées par ce dernier avant toute validation définitive de la transaction et donc avant la vente. <br>

Pour que les CGV acquièrent une valeur contractuelle, l'éditeur du site doit donc prévoir une modalité électronique d'acceptation (par exemple une fenêtre avec les CGV à accepter par une case à cocher et qui s'ouvre avant toute finalisation de la vente). <br>

En outre, ces CGV devront être mentionnées dans la facture que le vendeur enverra à son client, avec la commande. <br>

Enfin, il faudra indiquer les modalités de contact et les coordonnées du "service client" (en cas de réclamation, d'erreur ou de retard de livraison...). <br>


